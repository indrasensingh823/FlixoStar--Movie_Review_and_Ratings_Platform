        // Handle search functionality
        function handleSearch() {
            const query = searchInput.value.trim();
            if (!query) return;
            
            document.getElementById('home-section').style.display = 'none';
            movieDetails.style.display = 'none';
            watchlistSection.style.display = 'none';
            searchResultsSection.style.display = 'block';
            searchResultsGrid.innerHTML = '';
            searchLoader.style.display = 'block';
            
            fetch(`${API_URL}&s=${query}&type=movie`)
                .then(response => response.json())
                .then(data => {
                    searchLoader.style.display = 'none';
                    
                    if (data.Response === 'True') {
                        data.Search.forEach(movie => {
                            const movieCard = createMovieCard(movie);
                            searchResultsGrid.appendChild(movieCard);
                        });
                    } else {
                        searchResultsGrid.innerHTML = `<p class="no-results">No results found for "${query}"</p>`;
                    }
                })
                .catch(error => {
                    console.error('Error searching movies:', error);
                    searchLoader.style.display = 'none';
                    searchResultsGrid.innerHTML = `<p class="error">An error occurred while searching. Please try again.</p>`;
                });
        }

