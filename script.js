        // Get reviews for a specific movie
        function getMovieReviews(movieId) {
            const reviews = JSON.parse(localStorage.getItem('movieReviews')) || {};
            return reviews[movieId] || [];
        }

        // Calculate average rating from reviews
        function calculateAverageRating(reviews) {
            if (reviews.length === 0) return 0;
            const total = reviews.reduce((sum, review) => sum + review.rating, 0);
            return total / reviews.length;
        }

        // Render reviews for a movie card
        function renderCardReviews(reviews) {
            if (reviews.length === 0) return '<p class="no-reviews">No reviews yet</p>';
            
            return reviews.slice(0, 3).map(review => `
                <div class="card-review-item">
                    <div class="card-review-author">${review.author}</div>
                    <div class="card-review-content">${review.content}</div>
                </div>
            `).join('');
        }

