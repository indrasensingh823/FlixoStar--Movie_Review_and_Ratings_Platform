        function setCardRating(star, movieId) {
            const value = parseInt(star.getAttribute('data-value'));
            const stars = star.parentElement.querySelectorAll('.card-rating-star');
            
            // Save rating to local storage
            let ratings = JSON.parse(localStorage.getItem('movieRatings')) || {};
            ratings[movieId] = value;
            localStorage.setItem('movieRatings', JSON.stringify(ratings));
            
            // Update star display
            stars.forEach(s => {
                const starValue = parseInt(s.getAttribute('data-value'));
                if (starValue <= value) {
                    s.classList.add('active');
                } else {
                    s.classList.remove('active');
                }
            });
        }

